# média aritmética
num1 = input('Bem-vindo à calculadora de média aritmética de três notas INTEIRAS\nPara começar, digite a 1° nota = ')
num2 = input('Agora a segunda nota = ')
num3 = input('E, por último, a terceira nota = ')
num1 = int(num1)
num2 = int(num2)
num3 = int(num3)
soma = num1+num2+num3
med = soma/3
print('Perfeito! A média aritmética dos três números é',med)
